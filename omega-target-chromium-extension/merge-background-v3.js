#!/usr/bin/env node

/**
 * 合并背景脚本为 Manifest V3 Service Worker
 */

const fs = require('fs');
const path = require('path');

const buildDir = path.join(__dirname, 'build');
const jsDir = path.join(buildDir, 'js');

// 确保目录存在
if (!fs.existsSync(jsDir)) {
  console.error('Build directory not found. Please run grunt first.');
  process.exit(1);
}

// 背景脚本文件列表（按顺序）
const scripts = [
  'background_adapter.js',
  'log_error.js', 
  'omega_debug.js',
  'background_preload.js',
  'omega_pac.min.js',
  'omega_target.min.js',
  'omega_target_chromium_extension.min.js',
  'background.js'
];

// 备份原始 background.js
const originalBg = path.join(jsDir, 'background.js');
if (fs.existsSync(originalBg)) {
  fs.renameSync(originalBg, path.join(jsDir, 'background_original.js'));
}

// 合并脚本
let mergedContent = '// SwitchyOmega Manifest V3 Service Worker\n';
mergedContent += '// Generated by merge-background-v3.js\n\n';

for (const script of scripts) {
  const scriptPath = path.join(jsDir, script);
  if (script === 'background.js' && !fs.existsSync(scriptPath)) {
    // 使用备份的原始文件
    const backupPath = path.join(jsDir, 'background_original.js');
    if (fs.existsSync(backupPath)) {
      mergedContent += `\n// === ${script} ===\n`;
      mergedContent += fs.readFileSync(backupPath, 'utf8');
    }
  } else if (fs.existsSync(scriptPath)) {
    mergedContent += `\n// === ${script} ===\n`;
    mergedContent += fs.readFileSync(scriptPath, 'utf8');
  } else {
    console.warn(`Warning: ${script} not found`);
  }
}

// 写入最终的 background.js
fs.writeFileSync(path.join(jsDir, 'background.js'), mergedContent);

// 删除 background.html
const bgHtml = path.join(buildDir, 'background.html');
if (fs.existsSync(bgHtml)) {
  fs.unlinkSync(bgHtml);
}

console.log('✅ Background script merged for Manifest V3');
console.log(`📁 Output: ${path.join(jsDir, 'background.js')}`);
